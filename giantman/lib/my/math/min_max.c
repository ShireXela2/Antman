int get_max(int a, int b)
{
    return a > b ? a : b;
}

int get_min(int a, int b)
{
    return a < b ? a : b;
}

float get_fmax(float a, float b)
{
    return a > b ? a : b;
}

float get_fmin(float a, float b)
{
    return a < b ? a : b;
}
